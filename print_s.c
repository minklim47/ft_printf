#include "ft_printf.h"

int	print_s(char *str)
{
	int	i;

	if (!str)
	{
		print_s("(null)");
		return (6);
	}
	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
	return (i);
}
